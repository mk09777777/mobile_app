#!/bin/bash

# Fast node_modules cleanup script
# This script forcefully removes node_modules even with locked files

echo "🧹 Cleaning up node_modules..."

# Kill any running Metro bundler or Node processes
echo "Stopping Metro bundler..."
pkill -f "node.*metro" 2>/dev/null || true
pkill -f "react-native" 2>/dev/null || true
sleep 1

# Change to project directory
cd "$(dirname "$0")"

# Make all files writable (removes read-only flags)
echo "Making files writable..."
chmod -R u+w node_modules 2>/dev/null || true

# Remove corrupted directories with spaces first
echo "Removing corrupted directories..."
find node_modules -type d -name "* 2*" -exec rm -rf {} + 2>/dev/null || true
find node_modules -type d -name "*eslint 2*" -exec rm -rf {} + 2>/dev/null || true
find node_modules -type d -name "*metro 2*" -exec rm -rf {} + 2>/dev/null || true

# Use rsync trick for faster deletion (creates empty dir then syncs)
echo "Deleting node_modules (this may take a minute)..."
mkdir -p empty_dir
rsync -a --delete empty_dir/ node_modules/ 2>/dev/null || true
rmdir empty_dir 2>/dev/null || true

# Final cleanup
echo "Final cleanup..."
rm -rf node_modules 2>/dev/null || true

echo "✅ node_modules deleted successfully!"
echo ""
echo "Now run: npm install"

